SELECT 
    Н_УЧЕНИКИ.ГРУППА,
    COUNT(*) AS Количество_студентов
FROM 
    Н_УЧЕНИКИ
JOIN 
    Н_ПЛАНЫ ON Н_УЧЕНИКИ.ПЛАН_ИД = Н_ПЛАНЫ.ИД
JOIN 
    Н_ОТДЕЛЫ ON Н_ПЛАНЫ.ОТД_ИД = Н_ОТДЕЛЫ.ИД
WHERE 
    Н_ОТДЕЛЫ.КОРОТКОЕ_ИМЯ = 'КТиУ'
    AND EXTRACT(YEAR FROM Н_УЧЕНИКИ.НАЧАЛО) = 2011
GROUP BY 
    Н_УЧЕНИКИ.ГРУППА
HAVING 
    COUNT(*) > 10
ORDER BY 
    Количество_студентов DESC;