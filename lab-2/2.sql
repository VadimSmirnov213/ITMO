SELECT 
    Н_ЛЮДИ.ИМЯ,
    Н_ВЕДОМОСТИ.ИД,
    Н_СЕССИЯ.ЧЛВК_ИД
FROM 
    Н_ВЕДОМОСТИ
RIGHT JOIN 
    Н_ЛЮДИ ON Н_ЛЮДИ.ИД = Н_ВЕДОМОСТИ.ЧЛВК_ИД
RIGHT JOIN 
    Н_СЕССИЯ ON Н_ЛЮДИ.ИД = Н_СЕССИЯ.ЧЛВК_ИД
WHERE 
    (Н_ЛЮДИ.ОТЧЕСТВО < 'Владимирович' OR Н_ЛЮДИ.ОТЧЕСТВО IS NULL)
    AND Н_ВЕДОМОСТИ.ЧЛВК_ИД > 117219
    AND Н_СЕССИЯ.УЧГОД = '2001/2002';